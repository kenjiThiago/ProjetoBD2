CREATE TYPE TIPOAULA AS ENUM ('video', 'texto');

-- Tabela Aluno
CREATE TABLE Aluno (
    cpf             VARCHAR(11)     PRIMARY KEY,
    nome            VARCHAR(100)    NOT NULL,
    email           VARCHAR(100)    NOT NULL,
    senha           VARCHAR(100)    NOT NULL,
    data_nascimento DATE,
    plano           VARCHAR(50),
    forma_pagamento VARCHAR(50)
);

-- Tabela Professor
CREATE TABLE Professor (
    email           VARCHAR(100)    PRIMARY KEY,
    nome            VARCHAR(100)    NOT NULL,
    especializacao  VARCHAR(100)
);

-- Tabela Habilidade
CREATE TABLE Habilidade (
    id              INT             PRIMARY KEY,
    nome            VARCHAR(100),
    nivel           VARCHAR(50)
);

-- Tabela Curso
CREATE TABLE Curso (
    nome            VARCHAR(100)    PRIMARY KEY,
    descricao       TEXT,
    duracao         INT,
    nivel           VARCHAR(50),
    data_lancamento DATE,
    acesso          VARCHAR(50),
    categoria       VARCHAR(50)
);

--Tabela Habilidade_Curso
CREATE TABLE Habilidade_Curso (
    nome_curso     VARCHAR(100),
    id_habilidade  INT,

    PRIMARY KEY (nome_curso, id_habilidade),
    FOREIGN KEY (nome_curso)    REFERENCES Curso(nome),
    FOREIGN KEY (id_habilidade) REFERENCES Habilidade(id)
);

-- Tabela Aula
CREATE TABLE Aula (
    nome_curso         VARCHAR(100)    NOT NULL,
    nome                VARCHAR(100)    PRIMARY KEY,
    descricao           TEXT,
    duracao             INT,
    tipo                TIPOAULA,
    ordem_dentro_modulo INT,

    FOREIGN KEY (nome_curso)   REFERENCES Curso(nome)
);

-- Tabela de Relacionamento Assiste (Aluno-Aula)
CREATE TABLE Assiste (
    cpf_aluno       VARCHAR(11)     NOT NULL,
    nome_aula       VARCHAR(100)    NOT NULL,
    data_conclusao  DATE,

    PRIMARY KEY (cpf_aluno, nome_aula),
    FOREIGN KEY (cpf_aluno)   REFERENCES Aluno(cpf),
    FOREIGN KEY (nome_aula)   REFERENCES Aula(nome)
)

-- Tabela Empresa
CREATE TABLE Empresa (
    nome        VARCHAR(100)    PRIMARY KEY,
    localizacao VARCHAR(100),
    setor       VARCHAR(50),
    descricao   TEXT,
    porte       VARCHAR(50),
    sigla       VARCHAR(10)
);

-- Tabela Vaga
CREATE TABLE Vaga (
    id          INT         PRIMARY KEY,
    nome        VARCHAR(100),
    descricao   TEXT,
    empresa     VARCHAR(100),
    nivel       VARCHAR(50),
    modalidade  VARCHAR(50),
    salario     VARCHAR(50),
    tempo_aberta INT,
    prazo       DATE,

    FOREIGN KEY (empresa)   REFERENCES Empresa(nome)
);

-- Tabela de Relacionamento Estudam (Aluno-Curso)
CREATE TABLE Estuda (
    cpf_aluno       VARCHAR(11),
    nome_curso      VARCHAR(100),
    data_conclusao  DATE,
    nota            DECIMAL(5, 2),

    PRIMARY KEY (cpf_aluno, nome_curso),
    FOREIGN KEY (cpf_aluno)   REFERENCES Aluno(cpf),
    FOREIGN KEY (nome_curso)    REFERENCES Curso(nome)
);

-- Tabela de Relacionamento Ministram (Professor-Curso)
CREATE TABLE Ministra (
    email_professor VARCHAR(100),
    nome_curso      VARCHAR(100),

    PRIMARY KEY (email_professor, nome_curso),
    FOREIGN KEY (email_professor)   REFERENCES Professor(email),
    FOREIGN KEY (nome_curso)        REFERENCES Curso(nome)
);
    
-- Tabela de Relacionamento Se_inscreve (Aluno-Vaga)
CREATE TABLE Se_Inscreve (
    cpf_aluno   VARCHAR(11),
    id_vaga     INT,

    PRIMARY KEY (cpf_aluno, id_vaga),
    FOREIGN KEY (cpf_aluno)   REFERENCES Aluno(cpf),
    FOREIGN KEY (id_vaga)       REFERENCES Vaga(id)
);

--Tabela Habilidade_Vaga
CREATE TABLE Habilidade_Vaga (
    id_vaga        INT,
    id_habilidade  INT,

    PRIMARY KEY (id_vaga, id_habilidade),
    FOREIGN KEY (id_vaga)       REFERENCES Vaga(id),
    FOREIGN KEY (id_habilidade) REFERENCES Habilidade(id)
);

